  <h2>BIOGRAPHY AND ARTISTIC STATEMENT</h2>
  <p>Monique was born in eastern Ontario where she still lives.  Bilingual, she studied at Carleton University and l’Université du Québec in “Change Management”.  The greater part of her career was spent working in human resources and adult education.</p>
  <p>Since 2010 Monique was able to reconnect with her long held wish to express her creativity through visual art.  In doing so she discovered a true passion for painting.  Nature and history being big interests, her travels around the world mainly in Europe, Asia and America provided her the inspiration for her painting. Through her creations Monique evokes in us memories and sometimes dreams allowing the viewer to constantly rediscover meanings in her art.</p>
  <p>Although her preferred choice of medium is oil on canvas, she will occasionally incorporate acrylics and soft pastels in her art.  Monique is passionate about figurative and abstract art; she has therefore developed her own approach to combine both.   Although at times she may encounter setbacks or “painter’s block” the beauty of art is that we are often guided by our emotions of the moment that unleashes our creativity.  The result of these moments can translate into an art piece that is simply amazing and an indication that Monique’s creations are truly in harmony with her heart and soul.  Her technique as with most artists is constantly evolving. Mastering visual art for Monique is a tireless source of creation and challenge she embraces with determination and joy.  Each creation is a project of many hours, sometimes many days. </p>
