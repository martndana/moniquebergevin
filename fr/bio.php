  <h2>BIOGRAPHIE ET DÉMARCHE ARTISTIQUE</h2>
  <p>Monique est née à Casselman dans l’Est Ontarien ou elle réside toujours. Bilingue, elle a étudié en « Gestion du changement » à l’Université Carleton et à l’Université du Québec.  Elle a fait carrière en grande partie dans le domaine des ressources humaines et de l’éducation des adultes.</p>
  <p>Depuis 2010 Monique a renoué avec un plaisir d’enfance et un rêve de longue date, exprimer sa créativité par l’art visuel.  Elle a donc reprit le dessin, et par la même occasion a découvert un réel engouement pour la peinture. Passionnée par la nature et l’histoire ancienne, ses voyages à travers le monde, notamment en Europe, en Asie et en Amérique, lui fournissent l’inspiration pour créer ses œuvres.  Ses tableaux évoquent des souvenirs et quelque fois des rêves permettant au spectateur une constante découverte et interprétation de son art.  À travers ceux-ci elle exprime la beauté du moment présent.</p>
  <p>Son choix de médium est l’huile sur toiles, quoiqu’elle incorpore à l’occasion l’acrylique et les pastels à l’huile tendres dans son art.  Monique est une fervente de l’art figuratif et de l’art abstrait.  Elle a donc développé sont approche afin d’imbriquer ces deux styles.  Comme tous les artistes, il arrive qu’elle rencontre de la frustration, une sorte de « mur ».  Toutefois la beauté de l’art est que nous sommes souvent guidé par l’émotion du moment présent qui libère la créativité en nous.  Le résultat de ces moments se traduit en une œuvre superbe démontrant ainsi que les créations de Monique sont réellement en harmonie avec son cœur et son âme.  Sa technique, comme est le cas pour la plupart des artistes, est en évolution constante. Maitriser l’art visuel devient pour elle une source intarissable de création et de défis qu’elle aborde avec détermination et bonheur.  Chaque tableau nécessite un travail de plusieurs heures voir de plusieurs jours.</p>
  <br />
